package utils.vectors;

public class DimensionCordVector {
    public int width;
    public int height;
    public int x;
    public int y;

    public DimensionCordVector(int width, int height, int x, int y) {
        this.width = width;
        this.height = height;
        this.x = x;
        this.y = y;
    }
}